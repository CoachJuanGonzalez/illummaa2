/**
 * Get breadcrumb data for a specific page
 * @param page - Page identifier
 * @param language - 'en' or 'fr'
 * @param slug - Optional slug for dynamic pages (e.g., blog posts)
 */
export function getBreadcrumbConfig(
  page: keyof typeof breadcrumbConfig | 'blogPost',
  language: 'en' | 'fr' = 'en',
  slug?: string
) {
  if (page === 'blogPost' && slug) {
    return breadcrumbConfig.blogPost(slug)[language];
  }
  return breadcrumbConfig[page as keyof Omit<typeof breadcrumbConfig, 'blogPost'>][language];
}
